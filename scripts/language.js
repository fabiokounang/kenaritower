function applyLang(lang) {
  const t = I18N[lang] || I18N.en;

  // nav
  const navLinks = document.querySelectorAll("#navMenu a");
  navLinks.forEach((a, i) => {
    if (t.nav[i]) a.textContent = t.nav[i];
  });

  // hero
  const heroHeadline = document.getElementById("heroHeadline");
  const heroTagline = document.getElementById("heroTagline");
  if (heroHeadline) heroHeadline.textContent = t.heroHeadline;
  if (heroTagline) heroTagline.textContent = t.heroTagline;

  // about
  const aboutTitle = document.getElementById("aboutTitle");
  if (aboutTitle) aboutTitle.textContent = t.aboutTitle;

  const aboutSection = document.getElementById("about");
  if (aboutSection) {
    const h3 = aboutSection.querySelector(".about-text h3");
    const ps = aboutSection.querySelectorAll(".about-text p");
    if (h3) h3.textContent = t.aboutH3;
    if (ps[0]) ps[0].textContent = t.aboutDescription;
    if (ps[1]) ps[1].textContent = t.aboutP2;
    if (ps[2]) ps[2].textContent = t.aboutP3;
  }

  // gallery
  const gallerySection = document.getElementById("gallery");
  if (gallerySection) {
    const title = gallerySection.querySelector(".section-title");
    if (title) title.textContent = t.galleryTitle;

    const overlays = gallerySection.querySelectorAll(".gallery-overlay");
    overlays.forEach((el, i) => {
      if (t.galleryOverlay[i]) el.textContent = t.galleryOverlay[i];
    });
  }

  // facilities
  const facilitiesSection = document.getElementById("facilities");
  if (facilitiesSection) {
    const title = facilitiesSection.querySelector(".section-title");
    if (title) title.textContent = t.facilitiesTitle;

    const cards = facilitiesSection.querySelectorAll(".facility-card");
    cards.forEach((card, i) => {
      const h3 = card.querySelector("h3");
      const p = card.querySelector("p");
      const pair = t.facilityCards[i];
      if (pair) {
        if (h3) h3.textContent = pair[0];
        if (p) p.textContent = pair[1];
      }
    });
  }

  // services
  const servicesSection = document.getElementById("services");
  if (servicesSection) {
    const title = servicesSection.querySelector(".section-title");
    if (title) title.textContent = t.servicesTitle;

    const cards = servicesSection.querySelectorAll(".service-card");
    cards.forEach((card, i) => {
      const h3 = card.querySelector("h3");
      const p = card.querySelector("p");
      const pair = t.serviceCards[i];
      if (pair) {
        if (h3) h3.textContent = pair[0];
        if (p) p.textContent = pair[1];
      }
    });
  }

  // location
  const locationSection = document.getElementById("location");
  if (locationSection) {
    const title = locationSection.querySelector(".section-title");
    if (title) title.textContent = t.locationTitle;

    const h3 = locationSection.querySelector(".location-info h3");
    if (h3) h3.textContent = t.locationH3;

    const info = locationSection.querySelector(".location-info");
    if (info) {
      const ps = info.querySelectorAll("p");
      // p0: strong Address
      // p2: strong Strategic
      if (ps[0]) {
        const strong = ps[0].querySelector("strong");
        if (strong) strong.textContent = t.addressLabel;
      }
      if (ps[2]) {
        const strong2 = ps[2].querySelector("strong");
        if (strong2) strong2.textContent = t.strategicTitle;
      }
      if (ps[3]) ps[3].textContent = t.strategicDesc;
    }
  }

  // contact
  const contactSection = document.getElementById("contact");
  if (contactSection) {
    const title = contactSection.querySelector(".section-title");
    if (title) title.textContent = t.contactTitle;

    const h3 = contactSection.querySelector(".contact-info h3");
    if (h3) h3.textContent = t.contactH3;

    const items = contactSection.querySelectorAll(".contact-item");
    if (items[0]) {
      const s = items[0].querySelector("strong");
      if (s) s.textContent = t.phoneLabel;
    }
    if (items[1]) {
      const s = items[1].querySelector("strong");
      if (s) s.textContent = t.emailLabel;
    }
    if (items[2]) {
      const s = items[2].querySelector("strong");
      if (s) s.textContent = t.hoursLabel;
      const ps = items[2].querySelectorAll("p");
      if (ps[1]) ps[1].textContent = t.hoursValue;
    }

    const note = contactSection.querySelector(".contact-info p[style*='margin-top: 2rem']");
    if (note) note.textContent = t.contactNote;

    const form = document.getElementById("contactForm");
    if (form) {
      const labels = form.querySelectorAll("label");
      if (labels[0]) labels[0].textContent = t.form.name;
      if (labels[1]) labels[1].textContent = t.form.email;
      if (labels[2]) labels[2].textContent = t.form.message;

      const btn = form.querySelector(".submit-btn");
      if (btn) btn.textContent = t.form.send;
    }
  }

  // footer headings + rights (optional, kalau struktur kamu sama)
  const footer = document.querySelector("footer");
  if (footer) {
    const sections = footer.querySelectorAll(".footer-section");
    if (sections[1]) {
      const h = sections[1].querySelector("h3");
      if (h) h.textContent = t.footerQuickLinks;
    }
    if (sections[2]) {
      const h = sections[2].querySelector("h3");
      if (h) h.textContent = t.footerContactInfo;
    }
    const rights = footer.querySelector(".footer-bottom p");
    if (rights) rights.textContent = t.footerRights;
  }

  // set active button state
  const switcher = document.getElementById("langSwitcher");
  if (switcher) {
    const btns = switcher.querySelectorAll(".lang-btn");
    btns.forEach(b => b.classList.remove("is-active"));
    const activeBtn = switcher.querySelector(`.lang-btn[data-lang="${lang}"]`);
    if (activeBtn) activeBtn.classList.add("is-active");
  }

  document.documentElement.setAttribute("lang", lang);
  localStorage.setItem(STORAGE_KEY, lang);
}

function initLangSwitcher() {
  const switcher = document.getElementById("langSwitcher");
  if (!switcher) return;

  switcher.addEventListener("click", (e) => {
    const btn = e.target.closest(".lang-btn");
    if (!btn) return;
    const lang = btn.getAttribute("data-lang");
    applyLang(lang);
  });

  const saved = localStorage.getItem(STORAGE_KEY) || "en";
  applyLang(saved);
}

document.addEventListener("DOMContentLoaded", initLangSwitcher);
